    private void listarCategorias() {
        modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre Categoría");
        modelo.addColumn("Descripción");
        
        List<Categoria> lista = dao.findAll();       
        for (Categoria cat : lista) {
            Object[] fila = new Object[3];
            fila[0] = cat.getCategoriaID();
            fila[1] = cat.getNombreCategoria();
            fila[2] = cat.getDescripcion();
            modelo.addRow(fila);
        }
        TblCategoria.setModel(modelo);
    }
    private void limpiarFormulario() {
        TxtCodigo.setText("");
        TxtNombre.setText("");
        TxtDescripcion.setText("");
        TblCategoria.clearSelection();
        BtnAñadir.setEnabled(true);
        BtnEditar.setEnabled(false);
        BtnEliminar.setEnabled(false);
    }
private void tablaCategoriasMouseClicked(java.awt.event.MouseEvent evt) {
    int fila = TblCategoria.getSelectedRow();  
    if (fila != -1) {
        // Habilitamos los botones al seleccionar una fila
        BtnEditar.setEnabled(true);
        BtnEliminar.setEnabled(true);     
        // Pasamos los datos a los cuadros de texto
        TxtCodigo.setText(TblCategoria.getValueAt(fila, 0).toString());
        TxtNombre.setText(TblCategoria.getValueAt(fila, 1).toString());
        TxtDescripcion.setText(TblCategoria.getValueAt(fila, 2).toString());
    }
}
