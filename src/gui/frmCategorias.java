    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        // TODO add your handling code here:
Categoria cat = new Categoria();
    cat.setCategoriaID(Integer.parseInt(TxtCodigo.getText())); // Asume que tienes el ID en el TXT
    cat.setNombreCategoria(TxtNombre.getText());
    cat.setDescripcion(TxtDescripcion.getText());

    if (dao.update(cat) > 0) {
        JOptionPane.showMessageDialog(this, "Actualizado exitosamente");
        listarCategorias(); // Recargar la tabla
    }
    }//GEN-LAST:event_BtnEditarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        // TODO add your handling code here:
int id = Integer.parseInt(TxtCodigo.getText());
    int r = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar?");
   if (r == 0) {
        if (dao.delete(id) > 0) {
            JOptionPane.showMessageDialog(this, "Eliminado exitosamente");
            listarCategorias();
            TxtCodigo.setText("");
            TxtNombre.setText("");
            TxtDescripcion.setText("");
        }
    }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void TblCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblCategoriaMouseClicked
        // TODO add your handling code here:
        int fila = TblCategoria.getSelectedRow();

    if (fila != -1) {
        BtnEditar.setEnabled(true);
        BtnEliminar.setEnabled(true);

        TxtCodigo.setText(TblCategoria.getValueAt(fila, 0).toString());
        TxtNombre.setText(TblCategoria.getValueAt(fila, 1).toString());
        TxtDescripcion.setText(TblCategoria.getValueAt(fila, 2).toString());
    }
    }//GEN-LAST:event_TblCategoriaMouseClicked

